    #[test]
    fn check_multiply_u64() {
        assert_eq!(vec![6, 0], multiply_u64(&[2], &[3]));
        assert_eq!(vec![0, 1, 1], multiply_u64(&[1 << 32, 1 << 32], &[1 << 32]));
        assert_eq!(
            vec![
                338457285041528512,
                873741774122123434,
                1614279433659886989,
                2232618956921417485
            ],
            multiply_u64(&[2745895686766400144, 7520886206723962441], &[
                9006621652498007052,
                5476023620144815056
            ])
        );
    }
